package model;

/**
 * Enumeração que representa as categorias disponíveis para os produtos
 * cadastrados no sistema de estoque.
 * <p>
 * Cada valor descreve um tipo de item comercializado pela loja,
 * permitindo organizar e classificar os produtos.
 * </p>
 *
 * <p>As categorias podem ser usadas para filtros, relatórios,
 * exibições e regras de negócio específicas.</p>
 *
 * @author ViniMonster
 */
public enum Categoria {
    hardware,
    perifericos,
    acessorios,
    outrosProdutos
}
